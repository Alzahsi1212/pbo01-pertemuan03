package Branching;

public class Switch {
    public static void main(String[] args) {
        int hewan=3;
        //contoh 1
        switch (hewan){
            case 1:
                System.out.println("Ayam");
                break;
            case 2:
                System.out.println("Kucing");
                break;
            case 3:
                System.out.println("Naga");
                break;
            case 4:
                System.out.println("Kura-Kura");
        }
    }
}
